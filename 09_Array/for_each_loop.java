//FOR EACH LOOP
//used to transverse data structures;
//cannot modify/update elements; **

//it is basically a copy of array (box )
public class for_each_loop {
    public static void main(String[] args) {
        int arr[]={1,2,5,3,6};

        for(int ele : arr){           //said as for element in array 
            System.err.println(ele+" ");
        }
    }
}   
